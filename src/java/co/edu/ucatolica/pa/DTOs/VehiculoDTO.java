package co.edu.ucatolica.pa.DTOs;

/**
 *
 * @author otro
 */
public class VehiculoDTO {
    
    private String vin = null;
    private String matricula = null;
    private String anioFabricacion = null;
    private String cilindraje = null;
    private String tipoCombustible =  null;
    private String modelo = null;
    private String color  = null;
    private String numSillas = null ;
    private int idMarca = 0 ;
    private int idTipo = 0;
    private String paisFab = null;
    private int precioCosto = 0 ;
    private int precioVenta = 0 ;
    private String nombre = null ;

    public VehiculoDTO() {
    }

    public String getVin() {
        return vin;
    }

    public void setVin(String vin) {
        this.vin = vin;
    }

    public String getMatricula() {
        return matricula;
    }

    public void setMatricula(String matricula) {
        this.matricula = matricula;
    }

    public String getAnioFabricacion() {
        return anioFabricacion;
    }

    public void setAnioFabricacion(String anioFabricacion) {
        this.anioFabricacion = anioFabricacion;
    }

    public String getCilindraje() {
        return cilindraje;
    }

    public void setCilindraje(String cilindraje) {
        this.cilindraje = cilindraje;
    }

    public String getTipoCombustible() {
        return tipoCombustible;
    }

    public void setTipoCombustible(String tipoCombustible) {
        this.tipoCombustible = tipoCombustible;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getNumSillas() {
        return numSillas;
    }

    public void setNumSillas(String numSillas) {
        this.numSillas = numSillas;
    }

    public int getIdMarca() {
        return idMarca;
    }

    public void setIdMarca(int idMarca) {
        this.idMarca = idMarca;
    }

    public int getIdTipo() {
        return idTipo;
    }

    public void setIdTipo(int idTipo) {
        this.idTipo = idTipo;
    }

    public String getPaisFab() {
        return paisFab;
    }

    public void setPaisFab(String paisFab) {
        this.paisFab = paisFab;
    }

    public int getPrecioCosto() {
        return precioCosto;
    }

    public void setPrecioCosto(int precioCosto) {
        this.precioCosto = precioCosto;
    }

    public int getPrecioVenta() {
        return precioVenta;
    }

    public void setPrecioVenta(int precioVenta) {
        this.precioVenta = precioVenta;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

}
